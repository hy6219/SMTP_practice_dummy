DROP TABLE SENDMAILEX;

CREATE TABLE SENDMAILEX(
	USERID VARCHAR(30) CONSTRAINT PK_UID PRIMARY KEY,
	PWD    VARCHAR(30) NOT NULL,
	EMAIL  VARCHAR(100) NOT NULL,
	ENABLED CHAR(1)     DEFAULT 'N',
	CONSTRAINT CK_ENABLED CHECK(ENABLED IN ('N','Y')) 
);

COMMENT ON COLUMN SENDMAILEX.USERID IS '사용자 아이디';
COMMENT ON COLUMN SENDMAILEX.PWD IS '사용자 비밀번호';
COMMENT ON COLUMN SENDMAILEX.EMAIL IS '사용자 이메일';
COMMENT ON COLUMN SENDMAILEX.ENABLED IS '이메일 인증 후 계정 활성화 여부';

